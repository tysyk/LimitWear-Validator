def get_rule_message(
    key: str,
    *,
    fallback_title: str | None = None,
    fallback_message: str | None = None,
) -> dict[str, str]:
    item = RULE_MESSAGES.get(key)

    if item:
        return item

    return {
        "title": fallback_title or key,
        "message": fallback_message or "Потрібна додаткова перевірка.",
    }


def get_aggregate_reason(key: str) -> str:
    return AGGREGATE_REASONS.get(
        key,
        AGGREGATE_REASONS["manual_review_generic"],
    )


def get_explain_message(key: str) -> str:
    return EXPLAIN_MESSAGES.get(key, key)


def get_summary_headline(verdict: str) -> str:
    return SUMMARY_MESSAGES["headline"].get(verdict, "Аналіз завершено")


def get_summary_next_action(verdict: str) -> str:
    return SUMMARY_MESSAGES["next_action"].get(
        verdict,
        "Перевірте результат перед використанням.",
    )